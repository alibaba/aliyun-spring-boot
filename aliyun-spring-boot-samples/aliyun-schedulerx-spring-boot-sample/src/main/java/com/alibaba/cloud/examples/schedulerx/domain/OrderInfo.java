package com.alibaba.cloud.examples.schedulerx.domain;

/**
*
* @author xiaomeng.hxm
*/
public class OrderInfo {
    private String id;
    private int value;
    
    public OrderInfo(String id, int value) {
        this.id = id;
        this.value = value;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return "OrderInfo [id=" + id + ", value=" + value + "]";
    }
    
}
